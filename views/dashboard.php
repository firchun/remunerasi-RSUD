<script>
let chartRajal, chartRanap, chartRadiologi, chartLaboratorium;

function loadCharts() {
  const bulan = document.getElementById('filterBulan').value;
  const tahun = document.getElementById('filterTahun').value;

  let url = 'api/dashboard_kepatuhan.php';
  const params = [];
  if (bulan) params.push('bulan=' + bulan);
  if (tahun) params.push('tahun=' + tahun);
  if (params.length) url += '?' + params.join('&');

  fetch(url)
    .then(res => res.json())
    .then(data => {
      if (!data.success) return;

      if (chartRajal) chartRajal.destroy();
      if (chartRanap) chartRanap.destroy();
      if (chartRadiologi) chartRadiologi.destroy();
      if (chartLaboratorium) chartLaboratorium.destroy();

      const rajalLabels = data.rajal.slice(0, 15).map(r => r.nm_poli.replace(/\s+/g, ' ').trim());
      const rajalPasien = data.rajal.slice(0, 15).map(r => parseInt(r.jumlah_pasien));
      const rajalTindakan = data.rajal.slice(0, 15).map(r => parseInt(r.jumlah_tindakan));

      chartRajal = new Chart(document.getElementById('chartRajal'), {
        type: 'bar',
        data: {
          labels: rajalLabels,
          datasets: [
            { label: 'Jumlah Pasien', data: rajalPasien, backgroundColor: 'rgba(16, 185, 129, 0.7)', borderColor: 'rgba(16, 185, 129, 1)', borderWidth: 1, borderRadius: 4 },
            { label: 'Jumlah Tindakan', data: rajalTindakan, backgroundColor: 'rgba(59, 130, 246, 0.7)', borderColor: 'rgba(59, 130, 246, 1)', borderWidth: 1, borderRadius: 4 }
          ]
        },
        options: {
          responsive: true, maintainAspectRatio: false,
          plugins: { legend: { position: 'top', labels: { boxWidth: 12, padding: 12, font: { size: 11 } } } },
          scales: { x: { ticks: { font: { size: 9 }, maxRotation: 45 } }, y: { beginAtZero: true, ticks: { font: { size: 10 } } } }
        }
      });

      const ranapLabels = data.ranap.map(r => r.nm_bangsal);
      const ranapPasien = data.ranap.map(r => parseInt(r.jumlah_pasien));
      const ranapTindakan = data.ranap.map(r => parseInt(r.jumlah_tindakan));

      chartRanap = new Chart(document.getElementById('chartRanap'), {
        type: 'bar',
        data: {
          labels: ranapLabels,
          datasets: [
            { label: 'Jumlah Pasien', data: ranapPasien, backgroundColor: 'rgba(59, 130, 246, 0.7)', borderColor: 'rgba(59, 130, 246, 1)', borderWidth: 1, borderRadius: 4 },
            { label: 'Jumlah Tindakan', data: ranapTindakan, backgroundColor: 'rgba(139, 92, 246, 0.7)', borderColor: 'rgba(139, 92, 246, 1)', borderWidth: 1, borderRadius: 4 }
          ]
        },
        options: {
          responsive: true, maintainAspectRatio: false, indexAxis: 'y',
          plugins: { legend: { position: 'top', labels: { boxWidth: 12, padding: 12, font: { size: 11 } } } },
          scales: { x: { beginAtZero: true, ticks: { font: { size: 10 } } }, y: { ticks: { font: { size: 9 } } } }
        }
      });

      const radLabels = data.radiologi.map(r => r.bulan);
      const radPermintaan = data.radiologi.map(r => parseInt(r.permintaan));
      const radHasil = data.radiologi.map(r => parseInt(r.hasil));

      chartRadiologi = new Chart(document.getElementById('chartRadiologi'), {
        type: 'bar',
        data: {
          labels: radLabels,
          datasets: [
            { label: 'Permintaan', data: radPermintaan, backgroundColor: 'rgba(249, 115, 22, 0.7)', borderColor: 'rgba(249, 115, 22, 1)', borderWidth: 1, borderRadius: 4 },
            { label: 'Hasil', data: radHasil, backgroundColor: 'rgba(34, 197, 94, 0.7)', borderColor: 'rgba(34, 197, 94, 1)', borderWidth: 1, borderRadius: 4 }
          ]
        },
        options: {
          responsive: true, maintainAspectRatio: false,
          plugins: { legend: { position: 'top', labels: { boxWidth: 12, padding: 12, font: { size: 11 } } } },
          scales: { x: { ticks: { font: { size: 10 } } }, y: { beginAtZero: true, ticks: { font: { size: 10 } } } }
        }
      });

      const labLabels = data.laboratorium.map(r => r.bulan);
      const labPermintaan = data.laboratorium.map(r => parseInt(r.permintaan));
      const labHasil = data.laboratorium.map(r => parseInt(r.hasil));

      chartLaboratorium = new Chart(document.getElementById('chartLaboratorium'), {
        type: 'bar',
        data: {
          labels: labLabels,
          datasets: [
            { label: 'Permintaan', data: labPermintaan, backgroundColor: 'rgba(168, 85, 247, 0.7)', borderColor: 'rgba(168, 85, 247, 1)', borderWidth: 1, borderRadius: 4 },
            { label: 'Hasil', data: labHasil, backgroundColor: 'rgba(34, 197, 94, 0.7)', borderColor: 'rgba(34, 197, 94, 1)', borderWidth: 1, borderRadius: 4 }
          ]
        },
        options: {
          responsive: true, maintainAspectRatio: false,
          plugins: { legend: { position: 'top', labels: { boxWidth: 12, padding: 12, font: { size: 11 } } } },
          scales: { x: { ticks: { font: { size: 10 } } }, y: { beginAtZero: true, ticks: { font: { size: 10 } } } }
        }
      });
    })
    .catch(err => console.error('Gagal memuat grafik:', err));
}

document.addEventListener('DOMContentLoaded', function() {
  const tahunSelect = document.getElementById('filterTahun');
  const now = new Date();
  for (let y = now.getFullYear(); y >= now.getFullYear() - 5; y--) {
    const opt = document.createElement('option');
    opt.value = y;
    opt.textContent = y;
    tahunSelect.appendChild(opt);
  }

  document.getElementById('filterBulan').addEventListener('change', loadCharts);
  document.getElementById('filterTahun').addEventListener('change', loadCharts);

  loadCharts();
});
</script>
